// Research Assistant
$("#ra-bar").click(function () {
  if ($("#ra-content").hasClass("hidden")) {
    $("#ra-content").removeClass("hidden");
    $("#ra-plus").addClass("hidden");
    $("#ra-minus").removeClass("hidden");
    $("#ra-bar").addClass("acc-select");
  } else {
    $("#ra-content").addClass("hidden");
    $("#ra-plus").removeClass("hidden");
    $("#ra-minus").addClass("hidden");
    $("#ra-bar").removeClass("acc-select");
  }
})

